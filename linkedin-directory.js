function normalizeLinkedinValue(value) {
  const cleanValue = (value || '').trim();
  if (!cleanValue || cleanValue.toUpperCase() === 'NA') return '';
  if (!/^https?:\/\//i.test(cleanValue)) return '';

  return cleanValue.replace(/^http:\/\/(www\.)?linkedin\.com/i, 'https://www.linkedin.com');
}

function normalizeLookupKey(value) {
  return (value || '')
    .toString()
    .toLowerCase()
    .replace(/[’']/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

const LINKEDIN_DIRECTORY = (() => {
  const byNameCompany = new Map();
  const byName = new Map();

  LINKEDIN_DIRECTORY_TSV.split('\n').slice(1).forEach(line => {
    const [name = '', company = '', url = ''] = line.split('\t');
    const cleanUrl = normalizeLinkedinValue(url);
    if (!cleanUrl) return;

    const nameKey = normalizeLookupKey(name);
    const companyKey = normalizeLookupKey(company);
    if (!nameKey) return;

    byName.set(nameKey, cleanUrl);
    if (companyKey) byNameCompany.set(`${nameKey}__${companyKey}`, cleanUrl);
  });

  return { byNameCompany, byName };
})();

function getLinkedinUrlForHost(host) {
  if (!host || !host.linkedin) return '';
  if (typeof host.linkedin === 'string') return normalizeLinkedinValue(host.linkedin);

  const nameKey = normalizeLookupKey(host.name);
  const companyKey = normalizeLookupKey(host.company);

  return LINKEDIN_DIRECTORY.byNameCompany.get(`${nameKey}__${companyKey}`)
    || LINKEDIN_DIRECTORY.byName.get(nameKey)
    || '';
}
